from tabulate import tabulate

class Version:
    def __init__(self, version):
        self.version = version


    def __lt__(self, another): # <
        return str.__lt__(self.version, another.version)


    def __gt__(self, another): # >
        return str.__gt__(self.version, another.version)


    def __ne__(self, another): # !=
        return str.__ne__(self.version, another.version)


def main():
    to_test = [
        ("1.0.0", "2.0.0"),
        ("1.0.0", "1.42.0"),
        ("1.2.0", "1.2.42"),
        ("1.1.0-alpha", "1.2.0-alpha.1"),
        ("1.0.1b", "1.0.10-alpha.beta"),
        ("1.0.0-rc.1", "1.0.0"),
    ]

    for version_1, version_2 in to_test:
        print(tabulate(
                        [
                            [f'{version_1} < {version_2} ', Version(version_1) < Version(version_2)], 
                            [f'{version_1} > {version_2} ', Version(version_1) > Version(version_2)],
                            [f'{version_1} != {version_2} ', Version(version_1) != Version(version_2)]
                        ],
                            headers=['expression', 'result'], tablefmt='fancy_grid'
                         
                      )
            )

if __name__ == "__main__":
    main()

